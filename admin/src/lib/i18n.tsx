export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>(
    AdminLocalizationConfig.default,
  );

  // Load from localStorage on first client render
  useEffect(() => {
    if (typeof window === 'undefined') return;
    try {
      const stored = window.localStorage.getItem(STORAGE_KEY) as Language | null;
      if (stored === 'en' || stored === 'ar') {
        setLanguageState(stored);
        updateDocumentLanguage(stored);
      } else {
        updateDocumentLanguage(AdminLocalizationConfig.default);
      }
    } catch {
      updateDocumentLanguage(AdminLocalizationConfig.default);
    }
  }, []);

  const setLanguage = useCallback((lang: Language) => {
    setLanguageState(lang);
    if (typeof window !== 'undefined') {
      try {
        window.localStorage.setItem(STORAGE_KEY, lang);
      } catch {
        // ignore storage errors
      }
    }
    updateDocumentLanguage(lang);
  }, []);

  const t = useCallback(
    (key: string, fallback?: string) => {
      const dict = messages[language] || {};
      const value = dict[key];
      if (value) return value;
      return fallback ?? key;
    },
    [language],
  );

  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      setLanguage,
      t,
      dir: language === 'ar' ? 'rtl' : 'ltr',
    }),
    [language, setLanguage, t],
  );

  return (
    <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
  );
}

function updateDocumentLanguage(lang: Language) {
  if (typeof document === 'undefined') return;
  document.documentElement.lang = lang === 'ar' ? 'ar' : 'en';
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    throw new Error('useI18n must be used within I18nProvider');
  }
  return ctx;
}

export function useT() {
  const { t } = useI18n();
  return t;
}
